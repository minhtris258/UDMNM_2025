  const clampIndex = (i) => (i < 0 ? panels.length - 1 : (i >= panels.length ? 0 : i));
  const getBG = (i) => panels[i]?.dataset?.bg || "";

  // Preload t·∫•t c·∫£ ·∫£nh ƒë·ªÉ ƒë·ªïi n·ªÅn m∆∞·ª£t
  (function preloadAll() {
    panels.forEach(p => {
      const url = p?.dataset?.bg;
      if (!url) return;
      const img = new Image();
      img.decoding = "async";
      img.src = url;
    });
  })();

  // ƒê·ªïi n·ªÅn qua CSS var + class fade
  let fadeTimer = null;
  function setBG(url) {
    if (!url) return;
    // N·∫øu ƒëang fade th√¨ reset ƒë·ªÉ kh√¥ng nh·∫•p nh√°y
    if (fadeTimer) {
      clearTimeout(fadeTimer);
      fadeTimer = null;
    }
    // Th√™m l·ªõp ƒë·ªÉ CSS animate opacity l·ªõp bg
    visual.classList.add("is-fading");
    root.style.setProperty("--pb-bg", `url('${url}')`);
    // k·∫øt th√∫c fade
    fadeTimer = setTimeout(() => {
      visual.classList.remove("is-fading");
      fadeTimer = null;
    }, 220); // kh·ªõp t·ªëc ƒë·ªô trong CSS (~200ms)
  }

  // K√≠ch ho·∫°t tab/panel theo index
  function activate(i) {
    tabs.forEach(t => t.classList.remove("is-active"));
    panels.forEach(p => p.classList.remove("is-active"));
    tabs[i]?.classList.add("is-active");
    panels[i]?.classList.add("is-active");
  }

  // Chuy·ªÉn slide
  let lock = false;
  function show(to) {
    if (lock) return;
    const nextIdx = clampIndex(to);
    if (nextIdx === current) return;

    lock = true;
    activate(nextIdx);
    setBG(getBG(nextIdx));

    // unlock sau khi fade xong
    setTimeout(() => { lock = false; }, 260);
    current = nextIdx;
  }

